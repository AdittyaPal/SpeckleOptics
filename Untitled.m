rect=zeros(51);
rect(25:35,25:35)=ones(11);
surf(rect);
xlabel('\alpha');
ylabel('\beta');
zlabel('I(\alpha,\beta)');
y=fftshift(fft2(fftshift(rect)));
z=11*11;
corr_coeff=y/z;
auto_corr=(1+corr_coeff^2);
surf(abs(auto_corr));
xlabel('L\Deltax/\lambdaz');
ylabel('L\Deltay/\lambdaz');
zlabel('\Gamma_{I}');
power_spec=ifft2(auto_corr);
surf(fftshift(abs(power_spec)));
xlabel('f_{x}/(L/\lambdaz)');
ylabel('f_{y}/(L/\lambdaz)');
zlabel('G_{I}');
